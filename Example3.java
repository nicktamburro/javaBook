class Example3{
	public static void main(String args[]){

		int var;
		double x;

		var = 10;
		x = 10.0;

		System.out.println("Original var: " + var);
		System.out.println("Original x: " + x);
		System.out.println();

		var = var / 4;
		x = x / 4;

		System.out.println("new var: " + var);
		System.out.println("new x: " + x);
	}
}