class HowFarIsObject{
	public static void main(String args[]){

//timing the echo can tell us the distance to something;

		double echoTime;
		double distance;

		echoTime = 0.7;
		distance = echoTime / 2 * 1100;

		System.out.println("If you heard the echo in 0.7 seconds, the object is " + distance + " feet away!");



	}
}